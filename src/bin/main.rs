const CONNECTIONS_MAX: usize = 1;
const L2CAP_CHANNELS_MAX: usize = 1;

// This creates a default app-descriptor required by the esp-idf bootloader.
// For more information see: <https://docs.espressif.com/projects/esp-idf/en/stable/esp32/api-reference/system/app_image_format.html#application-description>
esp_bootloader_esp_idf::esp_app_desc!();

#[esp_rtos::main]
async fn main(spawner: Spawner) -> ! {
    // generator version: 1.0.1

    /* ========= GENERATED BY TEMPLATE START ========= */
    rtt_target::rtt_init_defmt!();

    let config = esp_hal::Config::default().with_cpu_clock(CpuClock::max());
    let peripherals = esp_hal::init(config);

    esp_alloc::heap_allocator!(#[esp_hal::ram(reclaimed)] size: 73744);
    // COEX needs more RAM - so we've added some more
    esp_alloc::heap_allocator!(size: 64 * 1024);

    let timg0 = TimerGroup::new(peripherals.TIMG0);
    esp_rtos::start(timg0.timer0);

    info!("Embassy initialized!");

    let radio_init = esp_radio::init().expect("Failed to initialize Wi-Fi/BLE controller");
    let (mut _wifi_controller, _interfaces) =
        esp_radio::wifi::new(&radio_init, peripherals.WIFI, Default::default())
            .expect("Failed to initialize Wi-Fi controller");
    // find more examples https://github.com/embassy-rs/trouble/tree/main/examples/esp32
    let transport = BleConnector::new(&radio_init, peripherals.BT, Default::default()).unwrap();
    let ble_controller = ExternalController::<_, 20>::new(transport);
    let mut resources: HostResources<DefaultPacketPool, CONNECTIONS_MAX, L2CAP_CHANNELS_MAX> =
        HostResources::new();
    let _stack = trouble_host::new(ble_controller, &mut resources);
    /* ========= GENERATED BY TEMPLATE END ========= */

    /* ========= flash info ========= */
    let mut flash = FlashStorage::new(peripherals.FLASH);
    println!("Flash size: {}mb", flash.capacity() / 1024 / 1024);

    let mut pt_mem = [0u8; partitions::PARTITION_TABLE_MAX_LEN];
    let pt = partitions::read_partition_table(&mut flash, &mut pt_mem).unwrap();

    for i in 0..pt.len() {
        let raw = pt.get_partition(i).unwrap();
        println!("{:?}", raw);
    }
    println!("");
    /* ========= flash info ========= */

    // Blink the power LED on startup
    spawner
        .spawn(blink_led_on_startup(peripherals.GPIO3))
        .unwrap();

    spawner
        .spawn(button_task(peripherals.ADC1, peripherals.GPIO1))
        .unwrap();

    loop {
        info!("Hello world!");
        Timer::after(Duration::from_secs(1)).await;
    }

    // for inspiration have a look at the examples at https://github.com/esp-rs/esp-hal/tree/esp-hal-v1.0.0/examples/src/bin
}

#[embassy_executor::task]
async fn blink_led_on_startup(led_gpio: GPIO3<'static>) {
    let mut led = gpio::Output::new(
        led_gpio,
        gpio::Level::Low,
        OutputConfig::default().with_drive_mode(gpio::DriveMode::OpenDrain),
    );

    for _ in 0..3 {
        led.set_high();
        Timer::after(Duration::from_millis(300)).await;
        led.set_low();
        Timer::after(Duration::from_millis(300)).await;
    }
}

#[embassy_executor::task]
async fn button_task(adc1: ADC1<'static>, analog_pin: GPIO1<'static>) {
    let mut adc1_config: AdcConfig<ADC1<'_>> = AdcConfig::new();
    let mut adc_pin1 = adc1_config.enable_pin(analog_pin, Attenuation::_11dB);
    let mut adc1 = Adc::new(adc1, adc1_config);

    loop {
        let val = block!(adc1.read_oneshot(&mut adc_pin1), Duration::from_millis(10)).unwrap();

        match val {
            v if v > 2400 && v < 2500 => println!("Dn Button pressed! ADC value: {}", val),
            v if v > 2900 && v < 3000 => println!("Up Button released! ADC value: {}", val),
            _ => { /* ignore other values */ }
        }

        Timer::after(Duration::from_millis(10)).await;
    }
}
